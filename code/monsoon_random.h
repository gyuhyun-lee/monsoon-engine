struct random_series
{
    u32 Index;
};

inline random_series
Seed(u32 Value)
{
    random_series Result = {};
    Result.Index= Value%ArrayCount(RandomNumberTable);

    return Result;
}

inline u32
GetNextRandomNumberInSeries(random_series *Series)
{
    Assert(Series->Index < ArrayCount(RandomNumberTable));

    u32 Result = RandomNumberTable[Series->Index++];

    if(Series->Index >= ArrayCount(RandomNumberTable))
    {
        Series->Index = 0;
    }

    return Result;
}

